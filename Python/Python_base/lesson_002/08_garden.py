# –≤ —Å–∞–¥—É —Å–æ—Ä–≤–∞–ª–∏ —Ü–≤–µ—Ç—ã
garden = ('—Ä–æ–º–∞—à–∫–∞', '—Ä–æ–∑–∞', '–æ–¥—É–≤–∞–Ω—á–∏–∫', '—Ä–æ–º–∞—à–∫–∞',
          '–≥–ª–∞–¥–∏–æ–ª—É—Å', '–ø–æ–¥—Å–æ–ª–Ω—É—Ö', '—Ä–æ–∑–∞')

# –Ω–∞ –ª—É–≥—É —Å–æ—Ä–≤–∞–ª–∏ —Ü–≤–µ—Ç—ã
meadow = ('–∫–ª–µ–≤–µ—Ä', '–æ–¥—É–≤–∞–Ω—á–∏–∫', '—Ä–æ–º–∞—à–∫–∞',
          '–∫–ª–µ–≤–µ—Ä', '–º–∞–∫', '–æ–¥—É–≤–∞–Ω—á–∏–∫', '—Ä–æ–º–∞—à–∫–∞')

# —Å–æ–∑–¥–∞–π—Ç–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ —Ü–≤–µ—Ç–æ–≤, –ø—Ä–æ–∏–∑—Ä–∞—Å—Ç–∞—é—â–∏—Ö –≤ —Å–∞–¥—É –∏ –Ω–∞ –ª—É–≥—É
garden_set = set(garden)
meadow_set = set(meadow)

# –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å –≤—Å–µ –≤–∏–¥—ã —Ü–≤–µ—Ç–æ–≤
all_flowers = garden_set.union(meadow_set)
print(', '.join(all_flowers))

# –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å —Ç–µ, –∫–æ—Ç–æ—Ä—ã–µ —Ä–∞—Å—Ç—É—Ç –∏ —Ç–∞–º –∏ —Ç–∞–º
garden_and_meadow = garden_set.intersection(meadow_set)
print(', '.join(garden_and_meadow))


# –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å —Ç–µ, –∫–æ—Ç–æ—Ä—ã–µ —Ä–∞—Å—Ç—É—Ç –≤ —Å–∞–¥—É, –Ω–æ –Ω–µ —Ä–∞—Å—Ç—É—Ç –Ω–∞ –ª—É–≥—É
only_garden = garden_set.difference(meadow_set)
print(', '.join(only_garden))

# –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å —Ç–µ, –∫–æ—Ç–æ—Ä—ã–µ —Ä–∞—Å—Ç—É—Ç –Ω–∞ –ª—É–≥—É, –Ω–æ –Ω–µ —Ä–∞—Å—Ç—É—Ç –≤ —Å–∞–¥—É
only_meadow = meadow_set.difference(garden_set)
print(', '.join(only_meadow))

